import turtle
fred=turtle.Turtle()
def square(color):
    fred.color(color)
    fred.forward(100)
    fred.right(90)
    fred.forward(100)
    fred.right(90)
    fred.forward(100)
    fred.right(90)
    fred.forward(100)
square("yellow")
square("green")
square("red")
square("blue")
